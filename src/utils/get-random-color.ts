const COLORS = [
  "red",
  "orange",
  "amber",
  "yellow",
  "lime",
  "green",
  "emerald",
  "teal",
  "cyan",
  "sky",
  "blue",
  "indigo",
  "violet",
  "purple",
  "fuchsia",
  "pink",
  "rose",
] as const;

export type Colors = (typeof COLORS)[number];

export const getRandomColor = (): Colors =>
  COLORS[Math.floor(Math.random() * COLORS.length)];
